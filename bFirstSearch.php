            <p>
                Breadth-First search(BFS) is a method for graph traversal. 
                Which actually happens to be the most common approach for graph traversal.
                <br /> <br />
                In BFS you begin at the first, otherwise known as the source, node and move through the graph layerwise.
                Meaning that you would visit all the nodes in your current level, or layer, before moving on to the next level.
